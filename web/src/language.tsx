export type TranslationKey = keyof typeof zh;
type Variables = Record<string, string | number>;

const STORAGE_KEY = "luanniao-locale";
let currentLocale: Locale = "zh-CN";

function navigatorLocale(): Locale {
  return typeof navigator !== "undefined" && navigator.language.toLowerCase().startsWith("en") ? "en-US" : "zh-CN";
}

export function readStoredLocale(): Locale {
  if (typeof localStorage === "undefined") return navigatorLocale();
  const stored = localStorage.getItem(STORAGE_KEY);
  return stored === "zh-CN" || stored === "en-US" ? stored : navigatorLocale();
}

export function getLocale(): Locale {
  return currentLocale;
}

export function translate(key: TranslationKey, variables?: Variables, locale: Locale = currentLocale): string {
  const template = locale === "en-US" ? en[key] : zh[key];
  return variables ? template.replace(/\{(\w+)\}/g, (match, name: string) => variables[name] === undefined ? match : String(variables[name])) : template;
}

export function formatDateTime(value?: string | number | Date, locale: Locale = currentLocale): string {
  if (value === undefined) return "-";
  const date = value instanceof Date ? value : new Date(value);
  return Number.isFinite(date.getTime()) ? new Intl.DateTimeFormat(locale, { dateStyle: "medium", timeStyle: "medium", hour12: false }).format(date) : "-";
}

export function formatRelativeTime(value?: string | number | Date, locale: Locale = currentLocale, now = Date.now()): string {
  if (value === undefined) return "-";
  const time = (value instanceof Date ? value : new Date(value)).getTime();
  if (!Number.isFinite(time)) return "-";
  const diff = now - time;
  if (diff < 60_000) return translate("common.justNow", undefined, locale);
  if (diff < 3_600_000) return translate("common.minutesAgo", { value: Math.floor(diff / 60_000) }, locale);
  if (diff < 86_400_000) return translate("common.hoursAgo", { value: Math.floor(diff / 3_600_000) }, locale);
  return translate("common.daysAgo", { value: Math.floor(diff / 86_400_000) }, locale);
}

export function formatNumber(value: number, locale: Locale = currentLocale, options?: Intl.NumberFormatOptions): string {
  return new Intl.NumberFormat(locale, options).format(value);
}

export function formatBytes(value: number, locale: Locale = currentLocale): string {
  if (value >= 1024 * 1024) return `${formatNumber(value / (1024 * 1024), locale, { maximumFractionDigits: 1 })} MB`;
  if (value >= 1024) return `${formatNumber(value / 1024, locale, { maximumFractionDigits: 1 })} KB`;
  return `${formatNumber(value, locale)} B`;
}

export function formatDuration(value: number, locale: Locale = currentLocale): string {
  return value >= 1000 ? `${formatNumber(value / 1000, locale, { minimumFractionDigits: 2, maximumFractionDigits: 2 })} s` : `${formatNumber(value, locale)} ms`;
}

interface LanguageContextValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  toggleLocale: () => void;
  t: (key: TranslationKey, variables?: Variables) => string;
  formatDate: (value?: string | number | Date) => string;
  formatRelative: (value?: string | number | Date) => string;
  formatNumber: (value: number, options?: Intl.NumberFormatOptions) => string;
  formatBytes: (value: number) => string;
  formatDuration: (value: number) => string;
}

const defaultContext: LanguageContextValue = {
  locale: "zh-CN",
  setLocale: () => undefined,
  toggleLocale: () => undefined,
  t: (key, variables) => translate(key, variables, "zh-CN"),
  formatDate: (value) => formatDateTime(value, "zh-CN"),
  formatRelative: (value) => formatRelativeTime(value, "zh-CN"),
  formatNumber: (value, options) => formatNumber(value, "zh-CN", options),
  formatBytes: (value) => formatBytes(value, "zh-CN"),
  formatDuration: (value) => formatDuration(value, "zh-CN")
};

const LanguageContext = createContext<LanguageContextValue>(defaultContext);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(readStoredLocale);

  const setLocale = useCallback((next: Locale) => {
    currentLocale = next;
    localStorage.setItem(STORAGE_KEY, next);
    setLocaleState(next);
  }, []);
  const toggleLocale = useCallback(() => setLocale(locale === "zh-CN" ? "en-US" : "zh-CN"), [locale, setLocale]);

  useEffect(() => {
    currentLocale = locale;
    document.documentElement.lang = locale;
    document.title = translate("app.title", undefined, locale);
  }, [locale]);

  const value = useMemo<LanguageContextValue>(() => ({
    locale,
    setLocale,
    toggleLocale,
    t: (key, variables) => translate(key, variables, locale),
    formatDate: (input) => formatDateTime(input, locale),
    formatRelative: (input) => formatRelativeTime(input, locale),
    formatNumber: (input, options) => formatNumber(input, locale, options),
    formatBytes: (input) => formatBytes(input, locale),
    formatDuration: (input) => formatDuration(input, locale)
  }), [locale, setLocale, toggleLocale]);

  return <LanguageContext.Provider value={value}><ConfigProvider locale={locale === "zh-CN" ? zhCN : enUS} theme={appTheme}>{children}</ConfigProvider></LanguageContext.Provider>;
}

export function useLanguage(): LanguageContextValue {
  return useContext(LanguageContext);
}

